def plus(number_one, number_two):
    return number_one + number_two


def minus(number_one, number_two):
    return number_one - number_two


def divide(number_one, number_two):
    return number_one / number_two


def multiply(number_one, number_two):
    return number_one * number_two


def get_hello(who):
    return "hello " + who
